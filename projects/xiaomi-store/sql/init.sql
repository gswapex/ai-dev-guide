  UNIQUE KEY `UK_ORDER_NO` (`ORDER_NO`) COMMENT '订单号唯一索引',
  KEY `IDX_USER_ID` (`USER_ID`) COMMENT '用户ID索引',
  KEY `IDX_STATUS` (`STATUS`) COMMENT '订单状态索引'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='订单信息表';

