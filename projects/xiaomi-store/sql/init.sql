-- 角色权限关联表
CREATE TABLE IF NOT EXISTS `ROLE_PERMISSION_REL` (
  `TID` BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT '自增ID',
  `ROLE_ID` BIGINT(20) NOT NULL COMMENT '角色ID',
  `PERMISSION_ID` BIGINT(20) NOT NULL COMMENT '权限ID',
