CREATE DATABASE IF NOT EXISTS xiaomi_store DEFAULT CHARSET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE xiaomi_store;

-- 用户信息表
CREATE TABLE IF NOT EXISTS `USER_INFO` (
  `TID` BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT '自增ID',
  `USER_ID` BIGINT(20) NOT NULL COMMENT '用户业务ID',
  `USER_NAME` VARCHAR(50) NOT NULL COMMENT '用户名',
  `PASSWORD` VARCHAR(100) NOT NULL COMMENT '密码',
  `ROLE_ID` BIGINT(20) COMMENT '角色ID',
  `STATUS` TINYINT(4) NOT NULL DEFAULT 0 COMMENT '用户状态：0-正常，1-禁用',
  `CREATE_TIME` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `UPDATE_TIME` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`TID`),
  UNIQUE KEY `UK_USER_NAME` (`USER_NAME`) COMMENT '用户名唯一索引',
  KEY `IDX_ROLE_ID` (`ROLE_ID`) COMMENT '角色ID索引'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='用户信息表';

-- 角色信息表
CREATE TABLE IF NOT EXISTS `ROLE_INFO` (
