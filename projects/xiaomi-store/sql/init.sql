  PRIMARY KEY (`TID`),
  UNIQUE KEY `UK_ROLE_NAME` (`ROLE_NAME`) COMMENT '角色名称唯一索引'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='角色信息表';

-- 权限信息表
CREATE TABLE IF NOT EXISTS `PERMISSION_INFO` (
